<?php

namespace Tests\Unit\Core\Domain;

use Core\Domain\Entity;

class Demo extends Entity
{
    protected $name;
    protected $email;

    public function __construct($id, $name, $email)
    {
        $this->id = $id;
        $this->name = $name;
        $this->email = $email;
    }

    public function getName()
    {
        return $this->name;
    }

    public function getEmail()
    {
        return $this->email;
    }
}
